def retrieve_tracks_from_targets(targets, top_k=5):
    tmp = retrieval_df.copy()

    target_z = {
        "tempo_bpm_z": zscore_value(targets["tempo_bpm"], "tempo_bpm"),
        "rms_mean_z": zscore_value(targets["rms_mean"], "rms_mean"),
        "centroid_mean_z": zscore_value(targets["centroid_mean"], "centroid_mean"),
    }

    tmp["retrieval_distance"] = (
        1.20 * (tmp["tempo_bpm_z"] - target_z["tempo_bpm_z"]) ** 2 +
        1.00 * (tmp["rms_mean_z"] - target_z["rms_mean_z"]) ** 2 +
        1.10 * (tmp["centroid_mean_z"] - target_z["centroid_mean_z"]) ** 2
    ) ** 0.5

    preferred = set(targets.get("preferred_genres", []))
    tmp["genre_bonus"] = tmp["genre"].apply(lambda g: -0.35 if g in preferred else 0.0) if preferred else 0.0
    tmp["final_score"] = tmp["retrieval_distance"] + tmp["genre_bonus"]

    cols = ["clip_id", "genre", "audio_path", "tempo_bpm", "rms_mean", "centroid_mean", "final_score"]
    return tmp.sort_values("final_score", ascending=True)[cols].head(top_k).reset_index(drop=True)

