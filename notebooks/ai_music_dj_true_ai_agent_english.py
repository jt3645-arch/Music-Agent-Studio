def aggregate_clip_probs(clip_ids, probs_np):
    clip_to_probs = defaultdict(list)
    for cid, p in zip(clip_ids, probs_np):
        clip_to_probs[cid].append(p)
    return {cid: np.mean(np.stack(arrs, axis=0), axis=0) for cid, arrs in clip_to_probs.items()}

def evaluate_loader(model, loader, true_clip_df):
    model.eval()
    all_losses, all_probs = [], []
    all_labels, all_clip_ids = [], []

    with torch.no_grad():
        for batch in loader:
            input_values = batch["input_values"].to(device)
            labels = batch["labels"].to(device)

            with torch.cuda.amp.autocast(enabled=(USE_MIXED_PRECISION and torch.cuda.is_available())):
                outputs = model(input_values=input_values, labels=labels)
                loss = outputs.loss
                logits = outputs.logits

            probs = torch.softmax(logits, dim=-1).detach().cpu().numpy()
            all_losses.append(loss.item())
            all_probs.append(probs)
            all_labels.extend(labels.detach().cpu().tolist())
            all_clip_ids.extend(batch["clip_ids"])

    all_probs = np.concatenate(all_probs, axis=0)
    seg_pred = all_probs.argmax(axis=1)
    seg_acc = accuracy_score(all_labels, seg_pred)

    clip_prob_map = aggregate_clip_probs(all_clip_ids, all_probs)
    clip_truth = true_clip_df.set_index("clip_id")["genre"].to_dict()

    clip_ids_sorted = sorted(clip_prob_map.keys())
    clip_probs = np.stack([clip_prob_map[cid] for cid in clip_ids_sorted], axis=0)
    clip_pred = clip_probs.argmax(axis=1)
    clip_true = np.array([label2id[clip_truth[cid]] for cid in clip_ids_sorted])

    clip_acc = accuracy_score(clip_true, clip_pred)

    return {
        "loss": float(np.mean(all_losses)),
        "seg_acc": float(seg_acc),
        "clip_acc": float(clip_acc),
        "clip_true": clip_true,
        "clip_pred": clip_pred,
        "clip_probs": clip_probs,
        "clip_ids": clip_ids_sorted,
    }

