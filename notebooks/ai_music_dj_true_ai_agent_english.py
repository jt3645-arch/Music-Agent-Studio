def load_audio_segment(path, start_sec, duration_sec, sr=MODEL_SR):
    y, _ = librosa.load(path, sr=sr, mono=True, offset=start_sec, duration=duration_sec)
    target_len = int(sr * duration_sec)
    if len(y) < target_len:
        y = np.pad(y, (0, target_len - len(y)))
    else:
        y = y[:target_len]
    return y.astype(np.float32)

class ASTSegmentDataset(Dataset):
    def __init__(self, segment_df, label2id, feature_extractor, training=False):
        self.df = segment_df.reset_index(drop=True)
        self.label2id = label2id
        self.feature_extractor = feature_extractor
        self.training = training

    def __len__(self):
        return len(self.df)

    def __getitem__(self, idx):
        row = self.df.iloc[idx]
        y = load_audio_segment(row["audio_path"], row["offset_sec"], SEGMENT_SECONDS, sr=MODEL_SR)

        if self.training:
            gain = np.random.uniform(0.85, 1.15)
            y = y * gain
            if np.random.rand() < 0.25:
                noise = np.random.normal(0, 0.002, size=y.shape).astype(np.float32)
                y = y + noise

        inputs = feature_extractor(y, sampling_rate=MODEL_SR, return_tensors="pt")
        input_values = inputs["input_values"].squeeze(0)
        label = self.label2id[row["genre"]]

        return {
            "input_values": input_values,
            "labels": torch.tensor(label, dtype=torch.long),
            "clip_id": row["clip_id"],
            "genre": row["genre"],
        }

