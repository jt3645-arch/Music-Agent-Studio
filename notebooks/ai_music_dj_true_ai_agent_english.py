def run_music_ai_agent(
    user_message: str,
    conversation_history: Optional[List[Any]] = None,
    *,
    force_answer: bool = True,
    use_web_search: bool = True,
    enable_critic: bool = True,
    model: Optional[str] = None,
    search_context_size: str = "medium",
) -> Dict[str, Any]:
    """
    Run the true AI music agent.

    Parameters
    ----------
    user_message:
        Any natural-language request.
    conversation_history:
        Optional previous turns. Accepts Gradio tuples or dicts like {"role": "user", "content": "..."}.
    force_answer:
        True means the agent should make assumptions and answer instead of blocking on clarification.
    use_web_search:
        True enables web search for real songs/facts. Use False only for offline testing.
    enable_critic:
        True runs a second model call to review the draft, then repairs if needed.
    model:
        OpenAI model name. Defaults to OPENAI_MODEL if defined, else gpt-5-mini.
    search_context_size:
        "low", "medium", or "high".
    """
    user_message = (user_message or "").strip()
    if not user_message:
        return {
            "status": "needs_input",
            "answer_markdown": "Please enter a music question or playlist request first.",
            "draft_answer": "",
            "critic_review": {},
            "web_citations": [],
        }

    history_text = _format_conversation_history(conversation_history)
    agent_input = f"""
Runtime settings:
- force_answer: {str(bool(force_answer)).lower()}
- use_web_search: {str(bool(use_web_search)).lower()}

Conversation history:
{history_text if history_text else "(none)"}

Latest user message:
{user_message}
""".strip()

    draft_response = _create_response(
        instructions=MUSIC_AGENT_INSTRUCTIONS,
        input_text=agent_input,
        model=model,
        use_web_search=use_web_search,
        search_context_size=search_context_size,
    )
    draft_answer = _response_output_text(draft_response)
    citations = _extract_response_url_citations(draft_response)

    critic_review = {
        "needs_revision": False,
        "issues": [],
        "repair_instructions": [],
        "passed_checks": [],
    }
    final_answer = draft_answer

    if enable_critic:
        review_input = json.dumps(
            {
                "runtime_settings": {
                    "force_answer": bool(force_answer),
                    "use_web_search": bool(use_web_search),
                },
                "conversation_history": history_text,
                "latest_user_message": user_message,
                "draft_answer": draft_answer,
            },
            ensure_ascii=False,
            indent=2,
        )

        review_response = _create_response(
            instructions=MUSIC_AGENT_CRITIC_INSTRUCTIONS,
            input_text=review_input,
            model=model,
            use_web_search=False,
            search_context_size=search_context_size,
        )
        review_text = _response_output_text(review_response)

        try:
            critic_review = _safe_json_loads_from_model_text(review_text)
        except Exception:
            critic_review = {
                "needs_revision": False,
                "issues": ["Critic JSON could not be parsed; keeping draft answer."],
                "repair_instructions": [],
                "passed_checks": [],
                "raw_review_text": review_text,
            }

        if critic_review.get("needs_revision"):
            repair_input = json.dumps(
                {
                    "conversation_history": history_text,
                    "latest_user_message": user_message,
                    "draft_answer": draft_answer,
                    "critic_review": critic_review,
                },
                ensure_ascii=False,
                indent=2,
            )
            repair_response = _create_response(
                instructions=MUSIC_AGENT_REPAIR_INSTRUCTIONS,
                input_text=repair_input,
                model=model,
                use_web_search=use_web_search,
                search_context_size=search_context_size,
            )
            final_answer = _response_output_text(repair_response)
            citations.extend(_extract_response_url_citations(repair_response))

    return {
        "status": "answered",
        "answer_markdown": final_answer,
        "draft_answer": draft_answer,
        "critic_review": critic_review,
        "web_citations": _dedupe_citations(citations),
        "model": model or DEFAULT_MUSIC_AGENT_MODEL,
        "force_answer": bool(force_answer),
        "use_web_search": bool(use_web_search),
    }


