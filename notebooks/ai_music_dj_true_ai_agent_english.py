def _response_output_text(response) -> str:
    """SDK-safe extraction of text from a Responses API result."""
    text = getattr(response, "output_text", None)
    if text:
        return str(text).strip()

    parts = []
    for item in getattr(response, "output", []) or []:
        for content in getattr(item, "content", []) or []:
            value = getattr(content, "text", None)
            if value:
                parts.append(str(value))
    return "\n".join(parts).strip()


def _extract_response_url_citations(response) -> List[Dict[str, str]]:
    """Best-effort extraction of web citation URLs from a Responses API result."""
    citations = []
    seen = set()

    for item in getattr(response, "output", []) or []:
        for content in getattr(item, "content", []) or []:
            for ann in getattr(content, "annotations", []) or []:
                url = getattr(ann, "url", None)
                title = getattr(ann, "title", None)

                nested = getattr(ann, "url_citation", None)
                if nested is not None:
                    url = url or getattr(nested, "url", None)
                    title = title or getattr(nested, "title", None)

                if url and url not in seen:
                    citations.append({"title": title or "", "url": url})
                    seen.add(url)

    return citations


def _dedupe_citations(citations: List[Dict[str, str]]) -> List[Dict[str, str]]:
    out = []
    seen = set()
    for c in citations or []:
        url = (c or {}).get("url")
        if not url or url in seen:
            continue
        out.append({"title": (c or {}).get("title", ""), "url": url})
        seen.add(url)
    return out


def _format_conversation_history(conversation_history: Optional[List[Any]]) -> str:
    """Accepts Gradio-style tuples or OpenAI-style dicts and turns them into plain text context."""
    if not conversation_history:
        return ""

    lines = []
    for turn in conversation_history:
        if isinstance(turn, dict):
            role = turn.get("role", "unknown")
            content = turn.get("content", "")
            if content:
                lines.append(f"{role}: {content}")
        elif isinstance(turn, (list, tuple)) and len(turn) >= 2:
            user_msg, assistant_msg = turn[0], turn[1]
            if user_msg:
                lines.append(f"user: {user_msg}")
            if assistant_msg:
                lines.append(f"assistant: {assistant_msg}")
        else:
            lines.append(str(turn))
    return "\n".join(lines).strip()


def _safe_json_loads_from_model_text(text: str) -> Dict[str, Any]:
    """Parse a JSON object from model text that may accidentally include code fences."""
    if not isinstance(text, str):
        raise ValueError("Model output is not a string.")
    s = text.strip()
    if s.startswith("```"):
        s = s.strip("`").strip()
        if s.lower().startswith("json"):
            s = s[4:].strip()

    start = s.find("{")
    end = s.rfind("}")
    if start < 0 or end <= start:
        raise ValueError("No JSON object found in model output:\n" + text[:1000])
    return json.loads(s[start:end + 1])


def _create_response(
    *,
    instructions: str,
    input_text: str,
    model: Optional[str] = None,
    use_web_search: bool = True,
    search_context_size: str = "medium",
):
    """Create a Responses API call, with fallback for accounts using web_search_preview."""
    client = _ensure_openai_client()
    model = model or DEFAULT_MUSIC_AGENT_MODEL

    kwargs = {
        "model": model,
        "instructions": instructions,
        "input": input_text,
    }

    if use_web_search:
        kwargs["tools"] = [{"type": "web_search", "search_context_size": search_context_size}]
        kwargs["tool_choice"] = "auto"

    try:
        return client.responses.create(**kwargs)
    except Exception as first_error:
        if not use_web_search:
            raise

        # Some environments/accounts still expose the preview tool name.
        kwargs["tools"] = [{"type": "web_search_preview", "search_context_size": search_context_size}]
        try:
            return client.responses.create(**kwargs)
        except Exception as second_error:
            raise RuntimeError(
                "Responses API call failed with both web_search and web_search_preview.\n"
                f"web_search error: {first_error}\n"
                f"web_search_preview error: {second_error}"
            )


